"""
Модуль обработки исключений для системы ArcticCyclone.

Определяет иерархию исключений, используемых в разных частях системы 
для обеспечения согласованной обработки ошибок.
"""

class ArcticCycloneError(Exception):
    """
    Базовый класс для всех исключений в системе ArcticCyclone.
    
    Все остальные исключения наследуются от этого класса, что позволяет
    обрабатывать все ошибки системы единообразно.
    """
    pass


class ConfigurationError(ArcticCycloneError):
    """
    Исключение, возникающее при проблемах с конфигурацией.
    
    Возникает при отсутствии необходимых параметров конфигурации, недопустимых
    значениях или при невозможности загрузить файл конфигурации.
    """
    pass


class DataSourceError(ArcticCycloneError):
    """
    Исключение, связанное с источниками данных.
    
    Возникает при проблемах доступа к источникам данных, ошибках аутентификации,
    отсутствии необходимых данных или неверном формате.
    """
    pass


class DataProcessingError(ArcticCycloneError):
    """
    Исключение, возникающее при обработке данных.
    
    Возникает при ошибках преобразования, фильтрации или анализа метеорологических данных.
    """
    pass


class DetectionError(ArcticCycloneError):
    """
    Исключение, связанное с обнаружением циклонов.
    
    Возникает при ошибках в алгоритмах обнаружения, недостаточном количестве данных
    или несоответствии критериев обнаружения.
    """
    pass


class TrackingError(ArcticCycloneError):
    """
    Исключение, связанное с отслеживанием циклонов.
    
    Возникает при ошибках в алгоритмах отслеживания, несогласованности
    временных шагов или несовместимости параметров.
    """
    pass


class VisualizationError(ArcticCycloneError):
    """
    Исключение, возникающее при визуализации.
    
    Возникает при ошибках создания изображений, графиков или экспорта визуализаций.
    """
    pass


class ExportError(ArcticCycloneError):
    """
    Исключение, связанное с экспортом данных.
    
    Возникает при ошибках записи данных в файлы, несовместимости форматов
    или ошибках в метаданных.
    """
    pass


class CredentialError(DataSourceError):
    """
    Исключение, связанное с учетными данными.
    
    Возникает при отсутствии необходимых учетных данных, ошибках аутентификации
    или недостаточных правах доступа.
    """
    pass


class ValidationError(ArcticCycloneError):
    """
    Исключение при проверке данных.
    
    Возникает при несоответствии данных ожидаемым форматам, диапазонам значений
    или критериям качества.
    """
    pass